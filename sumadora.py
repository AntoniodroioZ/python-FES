"""Programa que sume dos numeros dados por el usuario"""

print("Hola, vamos a hacer la suma de dos numeros enteros.")
num1 = int(input("Dime el primer numero a sumar: "))
num2 = int(input("Dime el segundo numero a sumar: "))

suma = num1 + num2

print("\n \t La suma de: ", num1, "y", num2, "es: ", suma)